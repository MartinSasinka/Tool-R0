def candidate_schema_errors(cand: Dict[str, Any], stage: str) -> List[str]:
    """Structural checks on a CHALLENGER candidate (pre-execution)."""
    errs: List[str] = []
    if not isinstance(cand, dict):
        return ["candidate is not a JSON object"]
    q = cand.get("question")
    if not isinstance(q, str) or len(q.split()) < 8:
        errs.append("question missing or too short")
    calls = cand.get("gold_calls")
    if not isinstance(calls, list) or not calls:
        errs.append("gold_calls missing/empty")
    else:
        for i, c in enumerate(calls):
            if not isinstance(c, dict) or not isinstance(c.get("name"), str) \
                    or not isinstance(c.get("arguments"), dict):
                errs.append(f"gold_calls[{i}] malformed")
    lo, hi = STAGES[stage]
    n = len(calls) if isinstance(calls, list) else 0
    if not (lo <= n <= hi):
        errs.append(f"call count {n} outside stage range [{lo},{hi}]")
    tools = cand.get("tool_names") or cand.get("tools")
    if not isinstance(tools, list) or not all(isinstance(t, str) for t in tools):
        errs.append("tool_names missing (must be a list of registry tool names)")
    motif = cand.get("motif_type")
    if motif not in MOTIFS:
        errs.append(f"motif_type must be one of {MOTIFS}")
    return errs


def question_leak_errors(question: str) -> List[str]:
    errs = []
    q_low = question.lower()
    for tok in _LEAK_TOKENS:
        if tok in q_low:
            errs.append(f"metadata token '{tok}' in question")
    if _VAR_IN_TEXT_RE.search(question):
        errs.append("$var$ reference syntax leaked into question")
    return errs


def looks_like_cot(text: Optional[str]) -> bool:
    """True when a rationale looks like leaked hidden chain-of-thought."""
    if not text:
        return False
    return len(str(text).split()) > 60


def final_row(*, sample_id: str, question: str, tools: List[Dict[str, Any]],
              gold_calls: List[Dict[str, Any]], observations: List[Any],
              gold_answer: Any, stage: str, motif_type: str, answer_type: str,
              generation_seed: int, models: Dict[str, str],
              solver_gap: Dict[str, Any], provenance: Dict[str, Any]
              ) -> Dict[str, Any]:
    """Assemble an ACCEPTED row in the exact spec §4 schema."""
    return {
        "sample_id": sample_id,
        "question": question,
        "tools": tools,
        "gold_calls": gold_calls,
        "observations": observations,
        "gold_answer": gold_answer,
        "num_calls": len(gold_calls),
        "stage": stage,
        "motif_type": motif_type,
        "answer_type": answer_type,
        "source": SOURCE_NAME,
        "generation_seed": generation_seed,
        "openrouter_models": {
            "challenger": models["challenger"],
            "weak_solver": models["weak_solver"],
            "strong_solver": models["strong_solver"],
            "judge": models["judge"],
        },
        "solver_gap": solver_gap,
        "quality": {
            "accepted": True,
            "rejection_reason": None,
            "validation_passed": True,
            "gold_replay_passed": True,
            "contamination_passed": True,
        },
        "provenance": provenance,
    }
