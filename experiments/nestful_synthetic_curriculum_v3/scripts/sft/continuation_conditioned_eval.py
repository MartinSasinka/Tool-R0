#!/usr/bin/env python3
"""Continuation-conditioned eval for the Stage2 continuation SFT experiment.

Tests whether the Stage2 continuation SIGNAL is learnable in isolation:

    input  = task + tools + GOLD call 1 + GOLD observation 1 (given — NOT
             generated by the model; identical construction to
             scripts/sft/sft_common.py::build_input_prefix_messages, i.e.
             exactly the SFT training input distribution)
    output = the model must generate (a) the second tool call and
             (b) the terminal empty-finish, via FREE generation
             (temperature configurable, default 0.0; no forced prefix beyond
             the given call 1; no teacher forcing of the model's own output).

This is deliberately NOT the same test as free ReAct eval (run.py --mode
rollout_eval / final_eval), which ALSO requires the model to find call 1 on
its own. Continuation-conditioned eval isolates "can it learn to continue
correctly given a correct start" from "can it also find that correct start
unaided" — the latter is what free ReAct eval measures (see
eval_stage2_sft.py, which runs both modes and reports them side by side).

Usage:
  python continuation_conditioned_eval.py \
      --val-path outputs/sft/stage2_continuation/val.jsonl \
      --checkpoint outputs/sft/stage2_continuation/run_.../adapter/epoch_1 \
      --out-dir outputs/sft/stage2_continuation/run_.../continuation_eval_sft \
      --eval-temperature 0.0
  # (omit --checkpoint to evaluate the base model)
"""
from __future__ import annotations

import argparse
import json
import os
import sys
from pathlib import Path
from typing import Any, Dict, List, Optional

HERE = Path(__file__).resolve().parent
sys.path.insert(0, str(HERE))

from sft_common import BASE_MODEL, MINIMAL, read_jsonl_raw  # noqa: E402

for _p in (str(MINIMAL),):
    if _p not in sys.path:
        sys.path.insert(0, _p)

from executor import ToolExecutor, matches_gold  # noqa: E402
from parser import parse_tool_call  # noqa: E402
from prompt import SYSTEM_PROMPT, build_user_content, format_tool_response  # noqa: E402
from rollout import _format_forced_call_text, generate_once  # noqa: E402
from run import build_registry, load_model_and_tokenizer  # noqa: E402


def parse_args() -> argparse.Namespace:
    ap = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--val-path", required=True, help="stage2_continuation/val.jsonl (SFT view).")
    ap.add_argument("--checkpoint", default=None, help="LoRA adapter dir; omit to eval the base model.")
    ap.add_argument("--out-dir", required=True)
    ap.add_argument("--base-model", default=BASE_MODEL)
    ap.add_argument("--eval-temperature", type=float, default=0.0)
    ap.add_argument("--top-p", type=float, default=0.95)
    ap.add_argument("--max-new-tokens", type=int, default=512)
    ap.add_argument("--load-in-4bit", action="store_true", default=True)
    ap.add_argument("--no-4bit", dest="load_in_4bit", action="store_false")
    ap.add_argument("--ibm-functions-dir", default=None,
                    help="Override IBM executable_functions dir (auto-detected by default).")
    ap.add_argument("--limit", type=int, default=None, help="Debug: cap number of val examples.")
    return ap.parse_args()


def _build_minimal_config(args) -> Dict[str, Any]:
    return {
        "model": {"base_model": args.base_model, "lora_adapter": None},
        "hardware": {"bf16": True, "load_in_4bit": args.load_in_4bit,
                    "gradient_checkpointing": False, "hf_device_map": "auto",
                    "use_flash_attention": False},
        "finetuning": {"method": "qlora" if args.load_in_4bit else "lora",
                       "lora_r": 16, "lora_alpha": 32, "lora_dropout": 0.05,
                       "target_modules": "auto", "load_in_4bit": args.load_in_4bit,
                       "bnb_4bit_quant_type": "nf4", "bnb_4bit_compute_dtype": "bfloat16",
                       "bnb_4bit_use_double_quant": True},
        "paths": {"ibm_functions_dir": args.ibm_functions_dir or
                  str(MINIMAL / "data" / "NESTFUL-main" / "data_v2" / "executable_functions")},
        "generation": {"temperature": args.eval_temperature, "top_p": args.top_p,
                       "max_new_tokens_eval": args.max_new_tokens},
        "executor": {"mode": "auto", "ibm_call_timeout": 30.0},
    }


def _task_from_record(record: Dict[str, Any]) -> Dict[str, Any]:
    task = {
        "task_id": record["sample_id"],
        "question": record["question"],
        "tools": record["tools"],
        "gold_calls": record["gold_calls"],
        "gold_answer": record["gold_answer"],
        "num_calls": len(record["gold_calls"]),
        "_gold_observations": record.get("gold_observations"),
    }
    return task


def run_continuation_eval(model, tokenizer, tasks: List[Dict[str, Any]], registry, config,
                          max_new_tokens: int, temperature: float, top_p: float):
    rows = []
    n = len(tasks)
    try:
        from tqdm import tqdm
        it = tqdm(tasks, desc="[continuation_eval]", unit="task")
    except ImportError:
        it = tasks

    for task in it:
        sid = task["task_id"]
        gold_calls = task["gold_calls"]
        gold_answer = task["gold_answer"]
        row: Dict[str, Any] = {"sample_id": sid, "executor_mode": None}
        try:
            gold_obs = task.get("_gold_observations")
            if not gold_obs or len(gold_obs) < 2:
                row["skip_reason"] = "missing_gold_observations"
                rows.append(row)
                continue

            executor = ToolExecutor(task, registry=registry, mode=config.get("executor", {}).get("mode", "auto"))
            row["executor_mode"] = executor.mode
            # Seed the executor's variable scope with the SAME precomputed gold
            # observation shown in the prompt (training-time parity — see
            # sft_common.build_input_prefix_messages), rather than
            # re-executing call 1 live: in `gold_replay` executor mode a live
            # re-execution of a NON-final call returns an opaque sentinel
            # marker (not the real observation), which would show the model a
            # different obs1 than it was trained on. This mirrors exactly what
            # ToolExecutor.execute() does to internal state (see executor.py).
            obs1 = gold_obs[0]
            label1 = (gold_calls[0].get("label") or "var_1").strip() or "var_1"
            executor.by_label[label1] = obs1
            executor.indexed.append(obs1)
            executor._turn = 1
            messages = [
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user", "content": build_user_content(task)},
                {"role": "assistant", "content": _format_forced_call_text(gold_calls[0])},
                {"role": "user", "content": format_tool_response(gold_calls[0], obs1)},
            ]

            gA = generate_once(model, tokenizer, messages, max_new_tokens,
                               temperature=temperature, top_p=top_p)
            row["turn_a_text"] = gA["text"]
            prA = parse_tool_call(gA["text"], lenient=True)

            no_tool_call = False
            too_few_calls = False
            parse_error = False
            wrong_tool = False
            wrong_argument = False
            predicted_call2: Optional[Dict[str, Any]] = None
            obs2 = None
            exec_error = None
            terminal_reached_turn_b = False
            turn_b_text = None
            num_predicted_continuation_calls = 0

            if prA.is_terminal:
                too_few_calls = True  # model thinks obs1 already is the final answer
                final_observation = obs1
            elif not prA.ok:
                parse_error = True
                no_tool_call = True
                final_observation = None
            else:
                predicted_call2 = prA.call
                num_predicted_continuation_calls = 1
                gold2 = gold_calls[1]
                name_ok = (predicted_call2.get("name") or "") == (gold2.get("name") or "")
                keys_ok = set((predicted_call2.get("arguments") or {}).keys()) == \
                    set((gold2.get("arguments") or {}).keys())
                wrong_tool = not name_ok
                wrong_argument = name_ok and not keys_ok

                res2 = executor.execute(predicted_call2)
                if res2.error is not None:
                    exec_error = res2.error
                    final_observation = None
                else:
                    obs2 = res2.observation
                    final_observation = obs2
                    messages.append({"role": "assistant", "content": gA["text"]})
                    messages.append({"role": "user",
                                     "content": format_tool_response(predicted_call2, obs2)})
                    gB = generate_once(model, tokenizer, messages, max_new_tokens,
                                       temperature=temperature, top_p=top_p)
                    turn_b_text = gB["text"]
                    prB = parse_tool_call(gB["text"], lenient=True)
                    if prB.is_terminal:
                        terminal_reached_turn_b = True
                    elif not prB.ok:
                        parse_error = True

            gold_obs2 = gold_obs[1]
            call2_correct = (predicted_call2 is not None and not wrong_tool and not wrong_argument
                             and exec_error is None and matches_gold(obs2, gold_obs2))
            strict_gold_trace_pass = bool(call2_correct and terminal_reached_turn_b)
            final_answer_pass = matches_gold(final_observation, gold_answer) if final_observation is not None else False

            row.update({
                "turn_b_text": turn_b_text,
                "predicted_call2": predicted_call2,
                "gold_call2": gold_calls[1],
                "exec_error": exec_error,
                "no_tool_call": no_tool_call,
                "too_few_calls": too_few_calls,
                "parse_error": parse_error,
                "wrong_tool": wrong_tool,
                "wrong_argument": wrong_argument,
                "terminal_reached_turn_b": terminal_reached_turn_b,
                "num_predicted_continuation_calls": num_predicted_continuation_calls,
                "strict_gold_trace_pass": strict_gold_trace_pass,
                "final_answer_pass": final_answer_pass,
                "final_observation": final_observation,
                "gold_answer": gold_answer,
            })
        except Exception as exc:  # noqa: BLE001 - one bad sample must never abort the run
            row["error"] = repr(exc)
        rows.append(row)

    def _rate(key: str) -> float:
        vals = [1.0 if r.get(key) else 0.0 for r in rows if "skip_reason" not in r and "error" not in r]
        return sum(vals) / len(vals) if vals else 0.0

    scored = [r for r in rows if "skip_reason" not in r and "error" not in r]
    metrics = {
        "num_tasks": n,
        "num_scored": len(scored),
        "num_skipped_gold_prefix_exec_error": sum(1 for r in rows if "skip_reason" in r),
        "num_errored": sum(1 for r in rows if "error" in r),
        "executor_mode": scored[0]["executor_mode"] if scored else "n/a",
        "final_answer_pass": _rate("final_answer_pass"),
        "strict_gold_trace_pass": _rate("strict_gold_trace_pass"),
        "too_few_calls_rate": _rate("too_few_calls"),
        "no_tool_call_rate": _rate("no_tool_call"),
        "parse_error_rate": _rate("parse_error"),
        "wrong_tool_rate": _rate("wrong_tool"),
        "wrong_argument_rate": _rate("wrong_argument"),
        "avg_predicted_continuation_calls": (
            sum(r.get("num_predicted_continuation_calls", 0) for r in scored) / len(scored)
            if scored else 0.0
        ),
    }
    return rows, metrics


def main() -> int:
    args = parse_args()
    os.makedirs(args.out_dir, exist_ok=True)
    config = _build_minimal_config(args)

    records = read_jsonl_raw(args.val_path)
    if args.limit:
        records = records[: args.limit]
    tasks = [_task_from_record(r) for r in records]
    print(f"[continuation_eval] loaded {len(tasks)} val tasks from {args.val_path}")
    print(f"[continuation_eval] checkpoint = {args.checkpoint or '<base model>'}")

    registry = build_registry(config)
    model, tokenizer = load_model_and_tokenizer(config, checkpoint=args.checkpoint, for_training=False)

    rows, metrics = run_continuation_eval(
        model, tokenizer, tasks, registry, config,
        max_new_tokens=args.max_new_tokens,
        temperature=args.eval_temperature, top_p=args.top_p,
    )

    with open(os.path.join(args.out_dir, "continuation_eval_trajectories.jsonl"), "w", encoding="utf-8") as fh:
        for r in rows:
            fh.write(json.dumps(r, ensure_ascii=False, default=str) + "\n")
    metrics["checkpoint"] = args.checkpoint
    metrics["eval_temperature"] = args.eval_temperature
    with open(os.path.join(args.out_dir, "continuation_eval_metrics.json"), "w", encoding="utf-8") as fh:
        json.dump(metrics, fh, indent=2, ensure_ascii=False)

    print(f"[continuation_eval] metrics: {json.dumps(metrics, indent=2)}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
